#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	len;
	int	i;

	len = ft_strlen(s);
	i = 0;
	while (i <= len)
	{	
		if ((char)c == s[i])
			return ((char *)&s[i]);
		i++;
	}
	return (0);
}
